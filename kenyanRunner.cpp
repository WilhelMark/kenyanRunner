#include <iostream>
#include <cmath>

int main() {
    // Переменные для хранения количества километров и темпов
    int kilometers;
    float total_time = 0; // Общее время в секундах

    // Запрашиваем у пользователя количество километров
    std::cout << "Привет, Сэм! Сколько километров ты сегодня пробежал? ";
    std::cin >> kilometers;

    // Проверяем корректность ввода
    if (kilometers <= 0) {
        std::cout << "Количество километров должно быть положительным числом." << std::endl;
        return 1; // Завершаем программу с ошибкой
    }

    // Цикл для ввода темпа на каждый километр
    for (int i = 1; i <= kilometers; ++i) {
        float pace;
        std::cout << "Какой у тебя был темп на километре " << i << "? ";
        std::cin >> pace;

        // Проверяем корректность ввода темпа
        if (pace < 0) {
            std::cout << "Темп не может быть отрицательным." << std::endl;
            return 1; // Завершаем программу с ошибкой
        }

        total_time += pace; // Суммируем общее время
    }

    // Рассчитываем средний темп в секундах на километр
    float average_pace = total_time / kilometers;

    // Преобразуем средний темп в минуты и секунды без использования static_cast
    int minutes = average_pace / 60; // Целая часть деления на 60
    int seconds = average_pace - (minutes * 60); // Остаток от деления

    // Округляем секунды до ближайшего целого числа
    seconds = round(seconds);

    // Если секунд больше 59, добавляем к минутам и корректируем секунды
    if (seconds >= 60) {
        minutes += seconds / 60; 
        seconds = seconds % 60; 
    }

    // Выводим результат
    std::cout << "Твой средний темп за тренировку: " 
              << minutes << " минуты " 
              << seconds << " секунд." 
              << std::endl;

    return 0;
}
